package com.oakspro.grocil;

public class OrdersData {
    String prod_price, pid, order_value, prod_qty, prod_total;

    public String getProd_price() {
        return prod_price;
    }

    public void setProd_price(String prod_price) {
        this.prod_price = prod_price;
    }

    public String getPid() {
        return pid;
    }

    public void setPid(String pid) {
        this.pid = pid;
    }

    public String getOrder_value() {
        return order_value;
    }

    public void setOrder_value(String order_value) {
        this.order_value = order_value;
    }

    public String getProd_qty() {
        return prod_qty;
    }

    public void setProd_qty(String prod_qty) {
        this.prod_qty = prod_qty;
    }

    public String getProd_total() {
        return prod_total;
    }

    public void setProd_total(String prod_total) {
        this.prod_total = prod_total;
    }
}
